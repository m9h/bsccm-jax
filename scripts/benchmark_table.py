def _dig(obj, dotted):
    for k in dotted.split("."):
        if isinstance(obj, dict) and k in obj:
            obj = obj[k]
        else:
            return None
    return obj


def render(results_dir=None):
    rows = []
    for ds, task, metric, ours, base, cmd, mfile, jpath in BENCHMARKS:
        live = ""
        if results_dir and mfile and jpath:
            hits = glob.glob(os.path.join(results_dir, mfile))
            if hits:
                try:
                    v = _dig(json.load(open(hits[0])), jpath)
                    if v is not None:
                        live = f" (live {v})"
                except (ValueError, OSError):
                    pass
        rows.append((ds, task, metric, ours + live, base, cmd))

    w = [max(len(r[i]) for r in rows + [("Dataset", "Task", "Metric", "Result", "Baseline / published", "Regenerate")])
         for i in range(6)]
    hdr = ("Dataset", "Task", "Metric", "Result", "Baseline / published", "Regenerate")
    line = lambda r: "| " + " | ".join(r[i].ljust(w[i]) for i in range(6)) + " |"
    out = ["# bsccm-jax benchmark results", "",
           "Regenerated by `scripts/benchmark_table.py`. Each row lists our result, the",
           "baseline/published number, and the command to reproduce it.", "",
           line(hdr), "| " + " | ".join("-" * w[i] for i in range(6)) + " |"]
    out += [line(r) for r in rows]
    return "\n".join(out) + "\n"


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--results-dir", default=None, help="dir of collected metrics JSON to pull live numbers")
    ap.add_argument("--write", default=None, help="write the table to this markdown file")
    args = ap.parse_args()
    table = render(args.results_dir)
    if args.write:
        os.makedirs(os.path.dirname(args.write) or ".", exist_ok=True)
        open(args.write, "w").write(table)
        print("wrote", args.write)
    else:
        print(table)


if __name__ == "__main__":
    main()
